"""
Push NER dataset to HuggingFace Hub.

Usage:
    python huggingface/push_dataset.py --doc cccd --repo username/vietnerm-cccd-dataset
    python huggingface/push_dataset.py --doc giay_ra_vien --repo username/vietnerm-grv-dataset
"""

import argparse
import json
import sys
from pathlib import Path
from typing import Optional

from huggingface_hub import HfApi, create_repo


def generate_dataset_card(
    doc_type: str,
    repo_id: str,
    dataset_dir: Path,
) -> str:
    """Generate a dataset card.

    Args:
        doc_type: Document type identifier.
        repo_id: HuggingFace repository ID.
        dataset_dir: Path to the dataset directory.

    Returns:
        Dataset card as a string.
    """
    doc_names = {
        "cccd": "Căn cước công dân (CCCD)",
        "giay_ra_vien": "Giấy ra viện",
        "vehicle_registration": "Đăng ký xe",
    }
    doc_name = doc_names.get(doc_type, doc_type)

    # Count samples if train.json exists
    train_count = 0
    test_count = 0
    train_path = dataset_dir / "train.json"
    test_path = dataset_dir / "test.json"
    if train_path.exists():
        with open(train_path, "r", encoding="utf-8") as f:
            train_count = len(json.load(f))
    if test_path.exists():
        with open(test_path, "r", encoding="utf-8") as f:
            test_count = len(json.load(f))

    # Load labels if available
    labels_info = ""
    labels_path = dataset_dir / "labels.json"
    if labels_path.exists():
        with open(labels_path, "r", encoding="utf-8") as f:
            labels = json.load(f)
            labels_info = f"\n### Labels\n{len(labels)} unique BIO labels\n"

    return f"""---
language: vi
tags:
  - ner
  - vietnamese
  - document-ai
  - {doc_type}
task_categories:
  - token-classification
size_categories:
  - 1K<n<10K
license: mit
---

# VietNerm - {doc_name} NER Dataset

Synthetic NER dataset for Vietnamese {doc_name} document entity extraction.

## Dataset Description

This dataset contains BIO-tagged token sequences for training NER models
on Vietnamese {doc_name} documents. Data is synthetically generated with
OCR noise simulation for robustness.

### Dataset Statistics

| Split | Samples |
|-------|---------|
| Train | {train_count} |
| Test  | {test_count} |
{labels_info}

## Format

Each sample contains:
- `tokens`: List of whitespace-tokenized words
- `ner_tags`: BIO labels for each token

## Usage

```python
from datasets import load_dataset

dataset = load_dataset("{repo_id}")
```

## Source

Generated by the [VietNerm](https://github.com/vietnerm/vietnerm) pipeline.
"""


def push_dataset(
    doc_type: str,
    repo_id: str,
    dataset_dir: Optional[str] = None,
    token: Optional[str] = None,
    private: bool = False,
) -> str:
    """Push dataset to HuggingFace Hub.

    Args:
        doc_type: Document type identifier.
        repo_id: HuggingFace repository ID.
        dataset_dir: Local dataset directory. Defaults to datasets/ner/{doc_type}/.
        token: HuggingFace API token.
        private: Whether to create a private repository.

    Returns:
        URL of the published dataset.
    """
    if dataset_dir is None:
        ds_path = Path("datasets") / "ner" / doc_type
    else:
        ds_path = Path(dataset_dir)

    if not ds_path.exists():
        print(f"[ERROR] Dataset directory not found: {ds_path}")
        sys.exit(1)

    api = HfApi(token=token)

    # Create repo
    print(f"[INFO] Creating/accessing dataset repo: {repo_id}")
    create_repo(repo_id, repo_type="dataset", exist_ok=True, private=private, token=token)

    # Generate dataset card
    print("[INFO] Generating dataset card...")
    dataset_card = generate_dataset_card(doc_type, repo_id, ds_path)
    readme_path = ds_path / "README.md"
    with open(readme_path, "w", encoding="utf-8") as f:
        f.write(dataset_card)

    # Upload
    print(f"[INFO] Uploading dataset from {ds_path}...")
    api.upload_folder(
        folder_path=str(ds_path),
        repo_id=repo_id,
        repo_type="dataset",
        token=token,
    )

    url = f"https://huggingface.co/datasets/{repo_id}"
    print(f"[SUCCESS] Dataset published to: {url}")
    return url


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Push VietNerm NER dataset to HuggingFace Hub"
    )
    parser.add_argument(
        "--doc", required=True,
        help="Document type (e.g., cccd, giay_ra_vien)",
    )
    parser.add_argument(
        "--repo", required=True,
        help="HuggingFace repo ID (e.g., username/vietnerm-cccd-dataset)",
    )
    parser.add_argument(
        "--dataset-dir",
        help="Path to dataset directory (default: datasets/ner/{doc}/)",
    )
    parser.add_argument(
        "--token",
        help="HuggingFace API token (default: uses cached token)",
    )
    parser.add_argument(
        "--private", action="store_true",
        help="Create a private repository",
    )

    args = parser.parse_args()
    push_dataset(
        doc_type=args.doc,
        repo_id=args.repo,
        dataset_dir=args.dataset_dir,
        token=args.token,
        private=args.private,
    )


if __name__ == "__main__":
    main()
